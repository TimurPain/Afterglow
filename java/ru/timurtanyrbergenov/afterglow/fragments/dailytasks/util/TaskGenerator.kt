    fun generateForDate(date: LocalDate): List<TaskTemplate> {
        // Выбираем 3 случайных языка любви
        val selectedLanguages = LoveLanguage.values().toList().shuffled().take(3)

        return selectedLanguages.map { language ->
            // Из шаблонов языка выбираем случайное задание
            val title = templates[language]?.random() ?: "Сделай что-то приятное"

            TaskTemplate(
                title = title,
                points = 10, // Можно сделать случайные очки: (8..15).random()
                loveLanguage = language,
                status = TaskStatus.ACTIVE,
                date = date
            )
        }
    }
